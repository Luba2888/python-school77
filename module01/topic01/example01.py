# Вывод данных
# print() - функция, которая выводит данные в скобках на экран

# 'Hello, world!' и 'Привет' - это литералы типа string(строка)
# Литерал - это запись в коде, представляющая собой фиксированное значение
print('Hello, world!')
print('Привет')

# '4' - это литерал типа string(строка)
# 4 - это литерал типа number(число)
print('4')
