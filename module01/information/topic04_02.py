# Операторы сравнения

a = int(input())
b = int(input())

# Равно
if a == b:
    print("Равны")

# Не равно
if a != b:
    print("Не равны")

# Больше
if a > b:
    print("Больше")

# Меньше
if a < b:
    print("Меньше")

# Больше или равно
if a >= b:
    print("Больше или равен")

# Меньше или равно
if a <= b:
    print("Меньше или равен")
