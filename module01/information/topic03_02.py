# Арифметические операции с присвоением
# Сложение
sum = 20
sum += 5
print(sum)

# Вычитание
diff = 20
diff -= 5
print(diff)

# Умножение
prod = 20
prod *= 5
print(prod)

# Деление
div = 20
div /= 5
print(div)

# Целочисленное деление
div_int = 20
div_int //= 6
print(div_int)

# Остаток от деления
mod = 20
mod %= 6
print(mod)

# Возведение в степень
exp = 20
exp **= 2
print(exp)


# Функции перевода чисел в различные системы счисления
number = 220

# Преобразование целого числа в двоичную строку
binary = bin(number)
print(binary)

# Преобразование целого числа в шестнадцатеричную строку
hexadecimal = hex(number)
print(hexadecimal)

# Преобразование целого числа в восьмеричную строку
octal = oct(number)
print(octal)


# Запись числа в различных системах счисления
# В двоичной системе
from_bin = 0b1011101
print(from_bin)

# В шестнадцатеричной системе
from_hex = 0xF48D
print(from_hex)

# В восьмеричной системе
from_oct = 0o7756
print(from_oct)
